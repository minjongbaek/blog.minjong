import fs from "fs";
import path from "path";

const CONTENTS_DIRECTORY = path.join(process.cwd(), "src/contents");
const OUTPUT_FILE = path.join(CONTENTS_DIRECTORY, "content-map.ts");

const generateContentMap = () => {
  const contentTypes = fs
    .readdirSync(CONTENTS_DIRECTORY, { withFileTypes: true })
    .filter((node) => node.isDirectory())
    .map((node) => node.name);

  const entries = contentTypes.map((contentType) => {
    const slugs = fs
      .readdirSync(path.join(CONTENTS_DIRECTORY, contentType), {
        withFileTypes: true,
      })
      .filter((node) => node.isDirectory())
      .map((node) => node.name);

    const inner = slugs
      .map(
        (slug) =>
          `    ${JSON.stringify(slug)}: () => import("./${contentType}/${slug}/index.mdx"),`,
      )
      .join("\n");

    return `  ${JSON.stringify(contentType)}: {\n${inner}\n  },`;
  });

  const content = `// This file is auto-generated by scripts/generate-content-map.mjs
// Do not edit manually.
import type { ComponentType } from "react";
import type { ContentMetadata } from "@/types/content";

export type ContentModule = {
  default: ComponentType;
  metadata: ContentMetadata;
};

export const contentMap = {
${entries.join("\n")}
} as unknown as Record<string, Record<string, () => Promise<ContentModule>>>;
`;

  fs.writeFileSync(OUTPUT_FILE, content, "utf8");

  const total = contentTypes.reduce((sum, type) => {
    return (
      sum +
      fs
        .readdirSync(path.join(CONTENTS_DIRECTORY, type), {
          withFileTypes: true,
        })
        .filter((node) => node.isDirectory()).length
    );
  }, 0);

  console.log(`Generated content-map.ts with ${total} entries.`);
};

generateContentMap();
